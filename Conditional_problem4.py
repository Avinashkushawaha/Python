# Determine if a fruit is ripe, overripe, or unripe based on its color.(e.g, Banana:Green - Unripe, Yellow - Ripe, Brown - Overripe)
fruits = "Banana"
color = input("Enter fruits color: ")

if fruits == "Banana": 
    if color == "Green":
        print("Unripe")

    elif color == "Yellow":
        print("Ripe")

    elif color == "Brown":    
        print("OverRipe")    

